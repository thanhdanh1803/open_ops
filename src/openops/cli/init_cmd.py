import typer
from rich.console import Console
from rich.panel import Panel
from rich.prompt import Confirm, Prompt

from openops.cli.main import app
from openops.config import OPENOPS_ENV_FILE, get_config

logger = logging.getLogger(__name__)
console = Console()


def _write_env_file(env_vars: dict[str, str]) -> None:
    """Write environment variables to the OpenOps .env file."""
    OPENOPS_ENV_FILE.parent.mkdir(parents=True, exist_ok=True)

    existing_vars: dict[str, str] = {}
    if OPENOPS_ENV_FILE.exists():
        with open(OPENOPS_ENV_FILE) as f:
            for line in f:
                line = line.strip()
                if line and not line.startswith("#") and "=" in line:
                    key, _, value = line.partition("=")
                    existing_vars[key.strip()] = value.strip()

    existing_vars.update(env_vars)

    with open(OPENOPS_ENV_FILE, "w") as f:
        f.write("# OpenOps Configuration\n")
        f.write("# Generated by 'openops init'\n\n")
        for key, value in sorted(existing_vars.items()):
            f.write(f"{key}={value}\n")

    logger.info(f"Updated env file: {OPENOPS_ENV_FILE}")


def _test_llm_connection(provider: str, api_key: str) -> bool:
    """Test connection to the LLM provider."""
    console.print("[dim]Testing connection...[/dim]")

    try:
        if provider == "anthropic":
            from langchain_anthropic import ChatAnthropic

            llm = ChatAnthropic(model="claude-haiku-4-5", api_key=api_key, max_tokens=10)
            llm.invoke("Hi")
        elif provider == "openai":
            from langchain_openai import ChatOpenAI

            llm = ChatOpenAI(model="gpt-5.4-nano", api_key=api_key, max_tokens=10)
            llm.invoke("Hi")
        elif provider == "google":
            from langchain_google_genai import ChatGoogleGenerativeAI

            llm = ChatGoogleGenerativeAI(
                model="gemini-2.5-flash-lite",
                google_api_key=api_key,
                max_output_tokens=10,
            )
            llm.invoke("Hi")
        elif provider == "deepseek":
            from langchain_openai import ChatOpenAI

            llm = ChatOpenAI(
                model="deepseek-chat",
                api_key=api_key,
                base_url="https://api.deepseek.com",
                max_tokens=10,
            )
            llm.invoke("Hi")
        else:
            console.print(f"[yellow]Skipping connection test for {provider}[/yellow]")
            return True

        return True
    except Exception as e:
        logger.debug(f"Connection test failed: {e}")
        return False


